  activeTrailerMode = "video";
  trailerEmbedMuted = false;
  trailerStatus.textContent = "Open the trailer tab on any title and it will load here without leaving this page.";
  trailerReadout.textContent = "Waiting for a trailer...";
  trailerMuteButton.textContent = "Mute";
