function normalize(value) {
  return String(value || "")
    .toLowerCase()
    .replaceAll(/[^a-z0-9]+/g, " ")
    .trim();
}

function escapeHtml(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function stripHtml(value) {
  const parser = new DOMParser();
  return parser.parseFromString(String(value || ""), "text/html").body.textContent || "";
}

function truncateText(value, maxLength = 230) {
  if (value.length <= maxLength) {
    return value;
  }
  const chunk = value.slice(0, maxLength);
  const stop = chunk.lastIndexOf(" ");
  return `${chunk.slice(0, stop > 0 ? stop : maxLength).trim()}...`;
}

