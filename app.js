function buildTagWeights() {
  const weights = new Map();
  const titles = [...watchlistCatalog.map((item) => item.title), ...addedOrder];
  titles.forEach((title) => {
    const item = catalogByTitle.get(title);
    if (!item) {
      return;
    }
    item.tags.forEach((tag) => {
      weights.set(tag, (weights.get(tag) || 0) + 1);
    });
  });
  for (const [title, rawPreference] of preferenceMap.entries()) {
    const item = catalogByTitle.get(title);
    const preference = Number(rawPreference);
    if (!item || !preference) {
      continue;
    }
    item.tags.forEach((tag) => {
      weights.set(tag, (weights.get(tag) || 0) + preference * 3);
    });
  }
  return weights;
}

function recommendationScore(item) {
  const weights = buildTagWeights();
  let score = 0;
  item.tags.forEach((tag) => {
    score += weights.get(tag) || 0;
  });
  score += item.reason ? Math.min(3, Math.ceil(item.reason.length / 80)) : 0;
  if (preferenceFor(item.title) === 1) {
    score += 8;
  }
  if (preferenceFor(item.title) === -1) {
    score -= 10;
  }
  if (watchedSet.has(item.title)) {
    score -= 2;
  }
  return score;
}

function recommendationWeight(item) {
  let weight = recommendationScore(item) + 14;
  if (watchedSet.has(item.title)) {
    weight -= 3;
  }
  return Math.max(1, weight);
}

function sampleRecommendations(items, count) {
  const pool = [...items];
  const selected = [];
  while (pool.length && selected.length < count) {
    const totalWeight = pool.reduce((sum, item) => sum + recommendationWeight(item), 0);
    let target = Math.random() * totalWeight;
    let index = 0;
    for (; index < pool.length; index += 1) {
      target -= recommendationWeight(pool[index]);
      if (target <= 0) {
        break;
      }
    }
    selected.push(pool.splice(Math.min(index, pool.length - 1), 1)[0]);
  }
  return selected;
}

function recommendationHistoryTitles(limit = 3) {
  return new Set((recommendationState.history || []).slice(0, limit).flat());
}

function recommendationFallbackPool(excluded = new Set()) {
  return recommendationCatalog
    .filter((item) => !addedSet().has(item.title) && !excluded.has(item.title))
    .sort((left, right) => {
      const scoreDelta = recommendationScore(right) - recommendationScore(left);
      if (scoreDelta !== 0) {
        return scoreDelta;
      }
      return canonicalName(left).localeCompare(canonicalName(right));
    });
}

function chooseRecommendationTitles() {
  const desiredCount = Math.min(8, recommendationCatalog.length);
  const recentlyShown = recommendationHistoryTitles();
  const current = new Set(recommendationState.current || []);
  let pool = recommendationFallbackPool(new Set([...recentlyShown, ...current]));

  if (pool.length < desiredCount) {
    pool = recommendationFallbackPool(current);
  }

  if (pool.length < desiredCount) {
    pool = recommendationFallbackPool();
  }

  const shortlist = pool.slice(0, Math.min(24, pool.length));
  const picked = sampleRecommendations(shortlist, desiredCount);
  if (picked.length < desiredCount) {
    const pickedTitles = new Set(picked.map((item) => item.title));
    recommendationFallbackPool(new Set(pickedTitles))
      .slice(0, desiredCount - picked.length)
      .forEach((item) => picked.push(item));
  }
  return picked.map((item) => item.title);
}

function ensureRecommendationState(forceRefresh = false) {
  const currentHidden = addedSet();
  const current = (recommendationState.current || []).filter((title) => catalogByTitle.has(title) && !currentHidden.has(title));
  if (!forceRefresh && current.length >= Math.min(8, recommendationCatalog.length)) {
    recommendationState.current = current;
    return;
  }

  const nextTitles = chooseRecommendationTitles().filter((title) => !currentHidden.has(title));
  recommendationState.current = nextTitles;
  recommendationState.history = [nextTitles, ...(recommendationState.history || []).filter((entry) => JSON.stringify(entry) !== JSON.stringify(nextTitles))].slice(0, 5);
  saveRecommendationState();
}

