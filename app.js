function sortedWatchlist(items) {
  const mode = sortBy.value;
  const clone = [...items];
  if (mode === "title") {
    clone.sort((a, b) => canonicalName(a).localeCompare(canonicalName(b)));
  } else if (mode === "year-desc") {
    clone.sort((a, b) => (b.year || 0) - (a.year || 0) || a.id.localeCompare(b.id));
  } else if (mode === "year-asc") {
    clone.sort((a, b) => (a.year || 9999) - (b.year || 9999) || a.id.localeCompare(b.id));
  }
  return clone;
}

