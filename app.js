function renderStreamingDetails(data, item) {
  streamingLine.textContent = data.summary || watchAvailability[item.title] || "No detailed streaming information is available right now.";
  offerGroups.replaceChildren();
  const grouped = new Map();
  (data.offers || []).forEach((offer) => {
    grouped.set(offer.kind, (grouped.get(offer.kind) || 0) + 1);
  });
  [...grouped.entries()].forEach(([kind, count]) => {
    const chip = document.createElement("span");
    chip.className = "offer-group";
    chip.textContent = `${kind}: ${count} option${count === 1 ? "" : "s"}`;
    offerGroups.appendChild(chip);
  });

  streamingList.replaceChildren();
  if (!data.offers || !data.offers.length) {
    const empty = document.createElement("p");
    empty.className = "streaming-note";
    empty.textContent = "No detailed provider rows came back for this title right now.";
    streamingList.appendChild(empty);
  } else {
    data.offers.forEach((offer) => {
      const row = document.createElement("div");
      row.className = "streaming-row";
      const left = document.createElement("div");
      const provider = document.createElement("div");
      provider.className = "streaming-provider";
      provider.textContent = offer.provider;
      const meta = document.createElement("div");
      meta.className = "streaming-meta";
      [offer.kind, offer.priceLabel, offer.billingPeriod, offer.quality].filter(Boolean).forEach((value) => {
        const badge = document.createElement("span");
        badge.className = "streaming-badge";
        badge.textContent = value;
        meta.appendChild(badge);
      });
      left.append(provider, meta);
      row.appendChild(left);

      if (offer.url) {
        const link = document.createElement("a");
        link.className = "button primary streaming-link";
        link.href = offer.url;
        link.target = "_blank";
        link.rel = "noreferrer";
        link.textContent = "Open";
        row.appendChild(link);
      }

      streamingList.appendChild(row);
    });
  }
  streamingSource.textContent = data.updatedAt
    ? `Live U.S. provider and price snapshot refreshed ${data.updatedAt}.`
    : "Detailed provider rows are shown from the latest successful lookup.";
}

async function loadStreaming(item) {
  const cacheKey = `${item.title}:${item.type}:${item.year || ""}`;
  if (streamingCache.has(cacheKey)) {
    renderStreamingDetails(streamingCache.get(cacheKey), item);
    return;
  }
  const requestId = ++streamingRequest;
  renderStreamingFallback(item);
  try {
    const params = new URLSearchParams({
      title: canonicalName(item),
      type: item.type,
      year: String(item.year || "")
    });
    const response = await fetch(`/api/watch-options?${params.toString()}`);
    if (!response.ok) {
      throw new Error(`Streaming API returned ${response.status}`);
    }
    const data = await response.json();
    streamingCache.set(cacheKey, data);
    if (requestId === streamingRequest && activeId === item.id) {
      renderStreamingDetails(data, item);
    }
  } catch {
    if (requestId === streamingRequest && activeId === item.id) {
      renderStreamingFallback(item);
      streamingSource.textContent = "Live price lookup missed on this attempt, so the saved snapshot is shown instead.";
    }
  }
}

