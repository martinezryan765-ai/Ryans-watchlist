function parseOfferGroups(line) {
  const groups = [];
  const patterns = [
    { label: "Streaming", regex: /streaming on ([^.]+?)(?:\.| It is also possible| or buy it)/i },
    { label: "Free With Ads", regex: /for free with ads on ([^.]+?)(?:\.| It is also possible)/i },
    { label: "Buy", regex: /buy (?:it as download|\"[^\"]+\" on|\"[^\"]+\" as download on)? ?([^.]+?)(?:\.| and to download| or rent it)/i },
    { label: "Rent", regex: /rent (?:it on |\"[^\"]+\" on )?([^.]+?)(?:\.| online)/i }
  ];
  for (const pattern of patterns) {
    const match = line.match(pattern.regex);
    if (!match) {
      continue;
    }
    const names = match[1]
      .replaceAll(" as download", "")
      .replaceAll(" online", "")
      .split(/,| or /)
      .map((part) => part.trim())
      .filter(Boolean);
    if (names.length) {
      groups.push(`${pattern.label}: ${names.join(", ")}`);
    }
  }
  return groups;
}

async function fetchWikipedia(item) {
  const response = await fetch(`https://en.wikipedia.org/api/rest_v1/page/summary/${item.wikiPage}`);
  if (!response.ok) {
    throw new Error(`Wikipedia returned ${response.status}`);
  }
  const data = await response.json();
  const description = data.description ? `${data.description}.` : "";
  const extract = data.extract || "No summary available yet.";
  return {
    summary: truncateText(`${description} ${extract}`.trim(), item.sourceGroup === "watchlist" ? 230 : 190),
    longSummary: `${description} ${extract}`.trim(),
    source: "Metadata via Wikipedia",
    poster: data.originalimage?.source || data.thumbnail?.source || "",
    year: item.year
  };
}

async function fetchTVMaze(item) {
  const response = await fetch(`https://api.tvmaze.com/singlesearch/shows?q=${encodeURIComponent(item.tvmazeQuery)}`);
  if (!response.ok) {
    throw new Error(`TVMaze returned ${response.status}`);
  }
  const data = await response.json();
  const summary = stripHtml(data.summary || "No summary available yet.");
  return {
    summary: truncateText(summary, item.sourceGroup === "watchlist" ? 230 : 190),
    longSummary: summary,
    source: "Metadata via TVMaze",
    poster: data.image?.original || data.image?.medium || "",
    ratingText: data.rating?.average ? `${data.rating.average}/10` : "",
    networkText: data.network?.name || data.webChannel?.name || "",
    year: data.premiered ? Number(data.premiered.slice(0, 4)) : item.year
  };
}

async function hydrateItem(item) {
  try {
    const meta = item.tvmazeQuery ? await fetchTVMaze(item) : await fetchWikipedia(item);
    Object.assign(item, meta);
  } catch {
    item.summary = item.note || "Live metadata could not be loaded for this title right now.";
    item.longSummary = item.summary;
    item.source = "Live metadata unavailable";
  }
  renderGrids();
  if (activeId && itemsById.get(activeId)?.title === item.title) {
    renderDetails(itemsById.get(activeId));
  }
}

function formatVoteCount(value) {
  const count = Number(value || 0);
  if (!count) {
    return "";
  }
  return new Intl.NumberFormat("en-US", { notation: "compact", maximumFractionDigits: 1 }).format(count);
}

async function hydrateImdbRatings() {
  const items = allCatalogItems.map((item) => ({
    label: item.title,
    title: canonicalName(item),
    type: item.type,
    year: item.year || ""
  }));
  try {
    const response = await fetch(`/api/imdb-ratings?items=${encodeURIComponent(JSON.stringify(items))}`);
    if (!response.ok) {
      throw new Error(`IMDb API returned ${response.status}`);
    }
    const data = await response.json();
    const ratings = data.items || {};
    allCatalogItems.forEach((item) => {
      const imdb = ratings[item.title];
      if (!imdb || !imdb.rating) {
        return;
      }
      const votesLabel = formatVoteCount(imdb.votes);
      item.ratingText = `IMDb ${imdb.rating}/10`;
      if (item.source && !item.source.includes("IMDb")) {
        item.source = votesLabel ? `${item.source} • IMDb rating (${votesLabel} votes)` : `${item.source} • IMDb rating`;
      }
    });
  } catch {
    return;
  }
  renderGrids();
  if (activeId) {
    renderDetails(itemsById.get(activeId));
  }
}

