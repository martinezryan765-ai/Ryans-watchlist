function refreshWatchlistVisibility() {
  const search = searchInput.value.trim().toLowerCase();
  const wantedType = typeFilter.value;
  const wantedStatus = statusFilter.value;
  let visible = 0;
  watchlistGrid.querySelectorAll(".card").forEach((card) => {
    const item = itemsById.get(card.dataset.id);
    if (!item) {
      return;
    }
    const matchesSearch = !search || card.dataset.title.includes(search);
    const matchesType = wantedType === "all" || card.dataset.type === wantedType;
    const isWatched = watchedSet.has(item.title);
    const matchesStatus =
      wantedStatus === "all" ||
      (wantedStatus === "watched" && isWatched) ||
      (wantedStatus === "unwatched" && !isWatched);
    const show = matchesSearch && matchesType && matchesStatus;
    card.classList.toggle("is-hidden", !show);
    if (show) {
      visible += 1;
    }
  });
  visibleCount.textContent = String(visible);
  watchedCount.textContent = String(watchedSet.size);
}

function activateTab(tabName) {
  activeTab = tabName;
  document.querySelectorAll(".tab-button").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.tab === tabName);
  });
  document.querySelectorAll(".tab-panel").forEach((panel) => {
    panel.classList.toggle("is-active", panel.id === `tab-${tabName}`);
  });
  const item = itemsById.get(activeId);
  if (!item) {
    return;
  }
  if (tabName === "trailer") {
    loadTrailer(item);
  } else {
    resetTrailer();
  }
  if (tabName === "streaming") {
    loadStreaming(item);
  }
}

function pickSomethingTonight() {
  const visibleCards = [...watchlistGrid.querySelectorAll(".card:not(.is-hidden)")];
  const unwatched = visibleCards.filter((card) => {
    const item = itemsById.get(card.dataset.id);
    return item && !watchedSet.has(item.title);
  });
  const pool = unwatched.length ? unwatched : visibleCards;
  if (!pool.length) {
    return;
  }
  const card = pool[Math.floor(Math.random() * pool.length)];
  showDetails(card.dataset.id);
}

